package com.apititles.main;

public class DetailsError {
	
	private String  title;
	
	private Long status;
	
	private Long timestapLong;
	
	

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public Long getStatus() {
		return status;
	}

	public void setStatus(Long status) {
		this.status = status;
	}

	public Long getTimestapLong() {
		return timestapLong;
	}

	public void setTimestapLong(Long timestapLong) {
		this.timestapLong = timestapLong;
	}

	
	
}
